package com.yedam.java.ch0202;

public class IntegerExample {

	public static void main(String[] args) {
		//변수선언 : 데이터타입 변수명
		// <=/=> 변수 초기화 : 변수가 처음 값을 가짐 이 때 메모리 할당 진행.
		
		// byte : 1byte = 8bit; => 기본 단위로 사용
		byte v1 = -128;
		byte v2 = -30;
		byte v3 = 3;
		byte v4 = 30;
		// byte v5 = 128; // 값의 범위를 벗어날 경우 못 담음.
		
		// int : 4byte => Default
		// long : 8byte; 
		long var1 = 1000000000000000000L;
	}

}
